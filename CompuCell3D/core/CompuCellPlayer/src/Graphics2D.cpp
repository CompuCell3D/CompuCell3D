#include "Graphics2D.h"

using namespace std;

Graphics2D::Graphics2D(QWidget *parent, const char *name): QLabel(parent),Graphics2DBase(name)
{

}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Graphics2D::~Graphics2D(){
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Graphics2D::preDrawTask(){
   setFixedSize(labelSize);
   pixmap2D=QPixmap(labelSize);

}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Graphics2D::produceImage(QImage & image){
    image=pixmap()->toImage();
   
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Graphics2D::postDrawTask(){
   setPixmap(pixmap2D);
   update();

}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Graphics2D::initPainter(){
   painterPtr->begin(&pixmap2D);
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Graphics2D::paintBackground(const QColor & _color){
   pixmap2D.fill(_color);
}

